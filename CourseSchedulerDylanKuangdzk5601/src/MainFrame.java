    private void ScheduleClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ScheduleClassButtonActionPerformed
        String coursecode = (String)selectClassComboBox.getSelectedItem();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String name = (String)selectStudentComboBox.getSelectedItem();
        String studentID = "";
        for (StudentEntry s:students){
            if ((s.getLastName() + "," + s.getFirstName()).equals(name)){
                studentID = s.getStudentID();
            }
        }
        String semester = (String)currentSemesterLabel.getText();
        Integer seats = ClassQueries.getClassSeats(semester,coursecode);
        Integer scheduledstudents = ScheduleQueries.getScheduledStudentCount(semester,coursecode);
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        if (seats > scheduledstudents){
            ScheduleQueries.addScheduleEntry(new ScheduleEntry(semester,coursecode,studentID,"scheduled",currentTimestamp));
            scheduleclasslabel.setText(name + " has been scheduled for the class");
        }
        else{
            ScheduleQueries.addScheduleEntry(new ScheduleEntry(semester,coursecode,studentID,"waitlisted",currentTimestamp));
            scheduleclasslabel.setText(name + " has been waitlisted for the class");
        }
        
    }//GEN-LAST:event_ScheduleClassButtonActionPerformed

