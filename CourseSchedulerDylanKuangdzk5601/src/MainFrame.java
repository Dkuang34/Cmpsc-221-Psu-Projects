    private void SubmitButtonDropStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonDropStudentActionPerformed
        DropStudentText.setText("");
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        StudentEntry student = new StudentEntry("","","");
        String name = (String)StudentDropComboBox.getSelectedItem();
        String studentID = "";
        for (StudentEntry s:students){
            if ((name).equals(s.getLastName() + "," + s.getFirstName())){
                studentID = s.getStudentID();
                student = StudentQueries.getStudent(studentID);
            }
        }   
        StudentQueries.dropStudent(studentID);
        DropStudentText.append(name + studentID + " was dropped from the list of students \n \n");
        rebuildDisplayStudentScheduleComboBoxes();        
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        for (String semester:semesters){
            ArrayList<ScheduleEntry> droppedschedule = ScheduleQueries.getScheduleByStudent(semester,studentID);
            DropStudentText.append("For Semester: " + semester + "\n");
            for (ScheduleEntry s: droppedschedule){
                String droppedclass = s.getCourseCode();
                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, droppedclass);
                if (s.getStatus().equals("scheduled")){
                    DropStudentText.append(name + " " + studentID + " has been dropped from " + droppedclass + "\n");
                    if (!ScheduleQueries.getWaitlistedStudentsByClass(semester,droppedclass).isEmpty()){
                        ArrayList <ScheduleEntry> waitlist = ScheduleQueries.getWaitlistedStudentsByClass(semester,droppedclass);
                        ScheduleEntry first = waitlist.get(0);
                        ScheduleQueries.updateScheduleEntry(first);
                        String firststudent = waitlist.get(0).getStudentID();
                        StudentEntry fs = StudentQueries.getStudent(firststudent);
                        DropStudentText.append(fs.getLastName() + "," + fs.getFirstName() + " "+ fs.getStudentID()+ " " + " has been scheduled into " + droppedclass + "\n");
                        }
                }
                else{
                    DropStudentText.append(name + studentID + " has been dropped from the waitlist for " + droppedclass + "\n");
                }
            }            
        }

        
        
    }//GEN-LAST:event_SubmitButtonDropStudentActionPerformed

